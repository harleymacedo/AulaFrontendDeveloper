//uso do if
var i = 10
if (i === 10) {
    console.log('É 10')
} else if (i === 5) {
    console.log('É 5')
} else {
    console.log('Nenhuma das opções')
}

//uso do switch
var j = 15
switch (j) {
    case 5:
        console.log('É 5')
        break
    case 10:
        console.log('É 10')
        break
    default:
        console.log('Nenhuma das opções')
}

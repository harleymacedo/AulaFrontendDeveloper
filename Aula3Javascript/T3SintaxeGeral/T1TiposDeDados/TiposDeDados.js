//Tipo string
var cidade = 'Crato'
console.log(typeof cidade)

//Tipo number
var km = 12
console.log(typeof km)

//Tipo boolean
var ligado = true
console.log(typeof ligado)

//Tipo objeto literal
var conta = {agencia: '1234-1', numero: '1234567-1'}
console.log(typeof conta)

//Tipo objeto array
var idades = [20, 45, 60]
console.log(typeof idades)

//Tipo undefined
var email = undefined
console.log(typeof email)
